/** Deep-merge override onto English base */
function merge(base, over) {
  if (!over) return base
  const out = { ...base }
  for (const k of Object.keys(over)) {
    if (
      over[k] &&
      typeof over[k] === 'object' &&
      !Array.isArray(over[k]) &&
      base[k] &&
      typeof base[k] === 'object'
    ) {
      out[k] = merge(base[k], over[k])
    } else {
      out[k] = over[k]
    }
  }
  return out
}

